import { CalendarController } from './calendar.controller.js';

/**
 * –ö–ª–∞—Å—Å –º–æ–¥—É–ª—è –∫–∞–ª–µ–Ω–¥–∞—Ä—è
 * –£–ø—Ä–∞–≤–ª—è–µ—Ç –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ–º –∫–∞–ª–µ–Ω–¥–∞—Ä—è –∏ –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ–º —Å —Å–æ–±—ã—Ç–∏—è–º–∏
 */
export class CalendarModule {
    /**
     * –°–æ–∑–¥–∞–µ—Ç —ç–∫–∑–µ–º–ø–ª—è—Ä –º–æ–¥—É–ª—è Calendar
     * @param {OrdinaApp} app - –≠–∫–∑–µ–º–ø–ª—è—Ä –≥–ª–∞–≤–Ω–æ–≥–æ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
     */
    constructor(app) {
        this.app = app;
        this.controller = new CalendarController(app);
        this.container = null;
        this.isLoaded = false;
        this.containerId = 'calendar-content';
    }

    /**
     * –ó–∞–≥—Ä—É–∑–∫–∞ –∏ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –º–æ–¥—É–ª—è
     * –û—Ç–æ–±—Ä–∞–∂–∞–µ—Ç –∫–∞–ª–µ–Ω–¥–∞—Ä—å —Ç–µ–∫—É—â–µ–≥–æ –º–µ—Å—è—Ü–∞ –∏ –∑–∞–≥—Ä—É–∂–∞–µ—Ç —Å–æ–±—ã—Ç–∏—è
     * –ü—Ä–∏ –ø–æ–≤—Ç–æ—Ä–Ω—ã—Ö –≤—ã–∑–æ–≤–∞—Ö –æ–±–Ω–æ–≤–ª—è–µ—Ç –¥–∞–Ω–Ω—ã–µ –±–µ–∑ –ø–æ–ª–Ω–æ–π –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∏
     * 
     * @async
     * @returns {Promise<void>}
     * @throws {Error} –ï—Å–ª–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –Ω–µ –Ω–∞–π–¥–µ–Ω –∏–ª–∏ –∑–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö –Ω–µ —É–¥–∞–ª–∞—Å—å
     */
    async load() {
        if (this.isLoaded) {
            await this.controller.refresh();
            return;
        }

        // –ü–æ–ª—É—á–∏—Ç—å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
        this.container = document.getElementById(this.containerId);
        if (!this.container) {
            console.error(`Container #${this.containerId} not found`);
            return;
        }

        // –ó–∞–≥—Ä—É–∑–∏—Ç—å —Å–æ–±—ã—Ç–∏—è –∏–∑ Firestore
        await this.controller.fetchData();

        // –û—Ç—Ä–µ–Ω–¥–µ—Ä–∏—Ç—å –∫–∞–ª–µ–Ω–¥–∞—Ä—å –∏ —Å–æ–±—ã—Ç–∏—è
        this.controller.render(this.container);

        // –£—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Å–æ–±—ã—Ç–∏–π
        this.controller.setupEventListeners(this.container);

        this.isLoaded = true;
    }

    /**
     * –í—ã–≥—Ä—É–∑–∫–∞ –º–æ–¥—É–ª—è –∏ –æ—á–∏—Å—Ç–∫–∞ —Ä–µ—Å—É—Ä—Å–æ–≤
     * –£–¥–∞–ª—è–µ—Ç –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Å–æ–±—ã—Ç–∏–π –∏ –æ—á–∏—â–∞–µ—Ç —Å–æ—Å—Ç–æ—è–Ω–∏–µ
     * 
     * @returns {void}
     */
    unload() {
        if (this.controller) {
            this.controller.cleanup();
        }
        this.isLoaded = false;
    }

    /**
     * –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö –º–æ–¥—É–ª—è
     * –ü–µ—Ä–µ–∑–∞–≥—Ä—É–∂–∞–µ—Ç —Å–æ–±—ã—Ç–∏—è –∏ –æ–±–Ω–æ–≤–ª—è–µ—Ç –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∫–∞–ª–µ–Ω–¥–∞—Ä—è
     * 
     * @async
     * @returns {Promise<void>}
     */
    async refresh() {
        await this.controller.refresh();
    }
}
