/**
 * News Template
 * HTML —à–∞–±–ª–æ–Ω—ã –¥–ª—è –º–æ–¥—É–ª—è –Ω–æ–≤–æ—Å—Ç–µ–π
 */

export class NewsTemplate {
  /**
   * –ì–ª–∞–≤–Ω—ã–π —à–∞–±–ª–æ–Ω –Ω–æ–≤–æ—Å—Ç–µ–π
   */
  static render(data) {
    const {
      articles,
      currentPage,
      currentCountry,
      currentCategory,
      hasNextPage,
      categories,
      countries
    } = data;

    return `
      <div class="news-container">
        ${this.renderControls({
          currentCountry,
          currentCategory,
          categories,
          countries
        })}
        ${articles.length > 0 
          ? this.renderArticles(articles)
          : this.renderEmpty()
        }
        ${this.renderPagination(currentPage, hasNextPage)}
      </div>
    `;
  }

  /**
   * –ü–∞–Ω–µ–ª—å —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è (—Ñ–∏–ª—å—Ç—Ä—ã)
   */
  static renderControls(data) {
    const { currentCountry, currentCategory, categories, countries } = data;

    return `
      <div class="news-controls">
        <div class="news-filters">
          <div class="filter-group">
            <label for="news-country-select" class="filter-label">–°—Ç—Ä–∞–Ω–∞:</label>
            <select id="news-country-select" class="filter-select">
              ${countries.map(country => `
                <option value="${country.value}" ${country.value === currentCountry ? 'selected' : ''}>
                  ${country.label}
                </option>
              `).join('')}
            </select>
          </div>

          <div class="filter-group">
            <label for="news-category-select" class="filter-label">–ö–∞—Ç–µ–≥–æ—Ä–∏—è:</label>
            <select id="news-category-select" class="filter-select">
              ${categories.map(category => `
                <option value="${category.value}" ${category.value === currentCategory ? 'selected' : ''}>
                  ${category.label}
                </option>
              `).join('')}
            </select>
          </div>

          <button id="news-refresh-btn" class="btn-refresh" title="–û–±–Ω–æ–≤–∏—Ç—å">
            üîÑ
          </button>
        </div>
      </div>
    `;
  }

  /**
   * –°–ø–∏—Å–æ–∫ —Å—Ç–∞—Ç–µ–π
   */
  static renderArticles(articles) {
    return `
      <div id="news-container" class="news-grid">
        ${articles.map(article => this.renderArticle(article)).join('')}
      </div>
    `;
  }

  /**
   * –û–¥–Ω–∞ —Å—Ç–∞—Ç—å—è
   */
  static renderArticle(article) {
    const title = this.escapeHtml(article.title || '–ë–µ–∑ –∑–∞–≥–æ–ª–æ–≤–∫–∞');
    const description = this.escapeHtml(article.description || '');
    const source = this.escapeHtml(article.source_id || '–ò—Å—Ç–æ—á–Ω–∏–∫');
    const imageUrl = article.image_url || '';
    const link = article.link || '#';
    const pubDate = article.pubDate 
      ? new Date(article.pubDate).toLocaleDateString('ru-RU', {
          day: 'numeric',
          month: 'long',
          year: 'numeric'
        })
      : '';

    return `
      <article class="news-card">
        ${imageUrl ? `
          <div class="news-image-wrapper">
            <img 
              src="${imageUrl}" 
              alt="${title}" 
              class="news-image"
              loading="lazy"
              onerror="this.parentElement.style.display='none'"
            >
          </div>
        ` : ''}
        
        <div class="news-content">
          <h3 class="news-title">${title}</h3>
          
          ${description ? `
            <p class="news-description">${description}</p>
          ` : ''}
          
          <div class="news-meta">
            <span class="news-source">${source}</span>
            ${pubDate ? `<span class="news-date">${pubDate}</span>` : ''}
          </div>
          
          ${link !== '#' ? `
            <a 
              href="${link}" 
              target="_blank" 
              rel="noopener noreferrer"
              class="news-link"
            >
              –ß–∏—Ç–∞—Ç—å –¥–∞–ª–µ–µ ‚Üí
            </a>
          ` : ''}
        </div>
      </article>
    `;
  }

  /**
   * –ü–∞–≥–∏–Ω–∞—Ü–∏—è
   */
  static renderPagination(currentPage, hasNextPage) {
    return `
      <div class="news-pagination">
        <button 
          id="news-prev-btn" 
          class="pagination-btn"
          ${currentPage <= 1 ? 'disabled' : ''}
        >
          ‚Üê –ü—Ä–µ–¥—ã–¥—É—â–∞—è
        </button>
        
        <span class="pagination-info">
          –°—Ç—Ä–∞–Ω–∏—Ü–∞ ${currentPage}
        </span>
        
        <button 
          id="news-next-btn" 
          class="pagination-btn"
          ${!hasNextPage ? 'disabled' : ''}
        >
          –°–ª–µ–¥—É—é—â–∞—è ‚Üí
        </button>
      </div>
    `;
  }

  /**
   * –ü—É—Å—Ç–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
   */
  static renderEmpty() {
    return `
      <div class="news-empty">
        <div class="empty-icon">üì∞</div>
        <p class="empty-message">–ù–æ–≤–æ—Å—Ç–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã</p>
        <p class="empty-hint">–ü–æ–ø—Ä–æ–±—É–π—Ç–µ –∏–∑–º–µ–Ω–∏—Ç—å —Ñ–∏–ª—å—Ç—Ä—ã</p>
      </div>
    `;
  }

  /**
   * –°–æ—Å—Ç–æ—è–Ω–∏–µ –∑–∞–≥—Ä—É–∑–∫–∏
   */
  static loading(data) {
    const { currentCountry, currentCategory, categories, countries } = data;

    return `
      <div class="news-container">
        ${this.renderControls({
          currentCountry,
          currentCategory,
          categories,
          countries
        })}
        <div class="news-loading">
          <div class="loading-spinner"></div>
          <p class="loading-text">–ó–∞–≥—Ä—É–∑–∫–∞ –Ω–æ–≤–æ—Å—Ç–µ–π...</p>
        </div>
      </div>
    `;
  }

  /**
   * –°–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
   */
  static error(message) {
    return `
      <div class="news-error">
        <div class="error-icon">‚ö†Ô∏è</div>
        <p class="error-message">${message || '–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –Ω–æ–≤–æ—Å—Ç–µ–π'}</p>
        <button class="btn-retry" onclick="location.reload()">–ü–æ–≤—Ç–æ—Ä–∏—Ç—å</button>
      </div>
    `;
  }

  /**
   * –≠–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ HTML
   */
  static escapeHtml(text) {
    const div = document.createElement('div');
    div.textContent = text;
    return div.innerHTML;
  }
}
